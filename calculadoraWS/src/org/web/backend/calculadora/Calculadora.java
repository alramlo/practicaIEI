package org.web.backend.calculadora;

public class Calculadora {
	public int suma(int i, int j) {
		return (i + j);
	}

	public int resta(int i, int j) {
		return (i - j);
	}

	public int multiplica(int i, int j) {
		return (i * j);
	}

	public int divide(int i, int j) {
		return (i / j);
	}
}
